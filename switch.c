#include<stdio.h>

int main(){
    int k=1;
    switch(k){
    case 0:
        printf("zero");
        break;
    case 1:
        printf("one");
        break;
    case 2:
        printf("two");
        break;
    case 3:
        printf("three");
        break;
    case 4:
        printf("four");
        break;
    default:
        printf(" this is not a valid number");
    }
    return 0;

}
