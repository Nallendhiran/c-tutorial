#include<stdio.h>

int main(){
    int i=1;
    int j=3;
    int k=i+j;
    printf("%d \n",k);
    int l=i-j;
    printf("%d \n",l);
    int m=i*j;
    printf("%d \n",m);
    int n=i/j;
    printf("%d \n",n);
    int o=i++;
    printf("%d \n",o);
    int p=j++;
    printf("%d \n",p);
    return 0;
}
